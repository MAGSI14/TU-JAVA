import java.util.*;
public class _07_1ToN {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.print("Enter number: ");
        int N = scanner.nextInt();
        for (int i = 1; i <= N; i++) {
            System.out.print(i + " ");
        }
    }
}
